function loadProjectImage(imgElement, src) {
    imgElement.style.opacity = '0';
    imgElement.style.transition = 'opacity 0.3s ease';
    
    const img = new Image();
    img.onload = () => {
        imgElement.src = src;
        imgElement.style.opacity = '1';
    };
    img.src = src;
}

